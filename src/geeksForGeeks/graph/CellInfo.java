package geeksForGeeks.graph;

class CellInfo {
    int row;
    int col;
    int time;

    public CellInfo(int row, int col, int time) {
        this.row = row;
        this.col = col;
        this.time = time;
    }
    public CellInfo(int row, int col) {
        this.row = row;
        this.col = col;
    }
}