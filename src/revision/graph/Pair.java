package revision.graph;

public class Pair {
    int first;
    int second;

    int third;

    public Pair(int first, int second) {
        this.first = first;
        this.second = second;
    }
    public Pair(int first, int second,int third) {
        this.first = first;
        this.second = second;
        this.third=third;
    }
}
